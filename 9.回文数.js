/**
 * @param {number} x
 * @return {boolean}
 */
var isPalindrome = function(x) {
    x = x > 0 ? x : x * -1;

    const str = `${x}`;

    let left = 0;
    let right = str.length - 1;
    while (left < right) {
        if (str[left] !== str[right]) {
            return false;
        }
        left++;
        right--;
    }
    return true;
};
